i = 5
while i < 50:  #while llop are infinite so it need to be terminated.
  print(i)
  i += 5  # in this case we are incrementing the value of i by 5 so it can reach 50 and break itself.
else:
  print('The loop is over')  # else only can be used when the loop has break statement.


